numero1 : int

def num1 (numero1):
    if numero1 == 1:
        return 'Primero'
    if numero1 == 2:
        return('Segundo')
    if numero1 == 3:
        return('Tercero')
    if numero1 == 4:
        return('Cuarto')
    if numero1 == 5:
        return('Quinto')
    if numero1 == 6:
        return('Sexto')
    if numero1 == 7:
        return('Septimo')
    if numero1 > 7:
        return('VACIO')